//#region cartaoUm
const cartaoUm = document.querySelector('.cartao-um');
const itemUm = document.querySelector('.main__lista__item-um');

cartaoUm.addEventListener('mouseover', () => {
     itemUm.style.color = '#DF7401';
     itemUm.style.textDecoration = 'underline';
});

cartaoUm.addEventListener('mouseout', () => {
     itemUm.style.color = 'black';
     itemUm.style.textDecoration = 'none'
});
//#endregion

